#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		if (!src[i])
		{
			ft_memset(dst + i, '\0', n - i);
			return (dst);
		}
		dst[i] = src[i];
		i++;
	}
	return (dst);
}
