#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*res;
	int		size;

	size = ft_strlen(s1);
	res = ft_memalloc(size + 1);
	if (!res)
		return (NULL);
	return (ft_memcpy(res, (char *)s1, size + 1));
}
